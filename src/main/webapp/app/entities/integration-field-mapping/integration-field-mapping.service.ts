import axios from 'axios';

import { type IIntegrationFieldMapping } from '@/shared/model/integration-field-mapping.model';

const baseApiUrl = 'api/integration-field-mappings';

export default class IntegrationFieldMappingService {
  public find(id: number): Promise<IIntegrationFieldMapping> {
    return new Promise<IIntegrationFieldMapping>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IIntegrationFieldMapping): Promise<IIntegrationFieldMapping> {
    return new Promise<IIntegrationFieldMapping>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IIntegrationFieldMapping): Promise<IIntegrationFieldMapping> {
    return new Promise<IIntegrationFieldMapping>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IIntegrationFieldMapping): Promise<IIntegrationFieldMapping> {
    return new Promise<IIntegrationFieldMapping>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
